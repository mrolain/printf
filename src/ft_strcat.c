#include "printf.h"

int	ft_strcat(char *dst, char*src)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	while (dst)
		i++;
	while (src)
	{
		dst[i] = src[i +j];
		i++;
	}
	return (i);
}